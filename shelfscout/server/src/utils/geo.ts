/**
 * Calculate distance between two lat/lng points in meters (Haversine).
 */
export function distanceMeters(
  lat1: number,
  lng1: number,
  lat2: number,
  lng2: number,
): number {
  const R = 6371e3;
  const toRad = (deg: number) => (deg * Math.PI) / 180;
  const dLat = toRad(lat2 - lat1);
  const dLng = toRad(lng2 - lng1);
  const a =
    Math.sin(dLat / 2) ** 2 +
    Math.cos(toRad(lat1)) * Math.cos(toRad(lat2)) * Math.sin(dLng / 2) ** 2;
  return R * 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));
}

/**
 * Convert PostGIS POINT to { lat, lng }.
 */
export function pointToLatLng(point: string): { lat: number; lng: number } {
  // PostGIS returns POINT(lng lat)
  const match = point.match(/POINT\(([^ ]+) ([^ ]+)\)/);
  if (!match) throw new Error(`Invalid POINT: ${point}`);
  return { lng: parseFloat(match[1]), lat: parseFloat(match[2]) };
}
